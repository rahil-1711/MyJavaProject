package encapsulation;

public class Encapsulation {

	// encapsulation ---> In java encapsulation is mechanism of wrapping the data members
				//		   and member functions together into single unit called class
				//		for encapsulation we need to declare variables as 'private'
				//		and member functions as 'public'
	
	
	private int a=10;		// declaring variables as private
	
	public void my()			// declaring method as public 
	{
		System.out.println(" this is my method");
	}
	
	
	
	
}
